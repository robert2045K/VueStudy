    <h1 id="3">歷史</h1>

    <p>
      W3C製作的早期HTML標誌 發展 提姆·柏內茲-李
      1980年，物理學家提姆·柏內茲-李在歐洲核子研究中心（CERN）在承包工程期間，為使CERN的研究人員使用並共享文件，他提出並建立原型系統ENQUIRE。1989年，柏內茲-李在一份備忘錄中提出一個基於網際網路的超文字系統[5]。他制定HTML並在1990年底寫出瀏覽器和伺服器軟體。同年，柏內茲-李與CERN的資料系統工程師羅伯特·卡里奧聯合為專案申請資助，但未被CERN正式批准。在他的個人筆記中[6]柏內茲-李列舉「一些使用超文字的領域」，並把百科全書列為第一項[7]。
      HTML的首次公開描述出現於一個名為HTML
      Tags[8]的檔案中，由提姆·柏內茲-李於1991年底提及[9][10]。它描述18個元素，包括HTML初始的、相對簡單的設計。除了超連結標籤外，其他設計都深受CERN內部一個以標準通用標記語言（SGML）為基礎的檔案格式SGMLguid的影響。這些元素仍有11個存在於HTML
      4中[11]。
      柏內茲-李認為HTML是SGML的一個應用程式。1993年中期網際網路工程任務組（IETF）發布首個HTML規範的提案：「超文本標記語言（HTML）」網際網路草案[12]，由柏內茲-李與丹·康納利撰寫。其中包括一個SGML文件類型定義來定義語法[13]。草案於6個月後過期，不過值得注意的是其對NCSA
      Mosaic瀏覽器嵌入線上圖像的自訂標籤的認可，這反映IETF把標準立足於成功原型的理念[14]。同樣，戴夫·拉格特在1993年末提出的與之競爭的網際網路草案「HTML+（超文字標記格式）」建議規範已經實現的功能，如表格與填寫表單[15]。
      在HTML和HTML+的草案於1994年初到期後，IETF建立一個HTML工作群組，並在1995年完成"HTML
      2.0"，這是第一個旨在成為對其後續實現標準的依據的HTML規範[16]。
      在IETF的主導下，HTML標準的進一步發展因競爭利益而遭受停滯。自1996年起，HTML規範一直由全球資訊網協會（W3C）維護，並由商業軟體廠商出資[17]。不過在2000年，HTML也成為國際標準（ISO/
      IEC15445：2000）。HTML
      4.01於1999年末發布，進一步的勘誤版本於2001年發布。2004年，網頁超文字應用技術工作小組（WHATWG）開始開發HTML5，並在2008年與W3C共同提出，2014年10月28日完成標準化[18]。
    </p>
