      3.2完全去除數學公式，協調各種專有擴充，並採用網景設計的大多數視覺標記標籤。由於兩家公司達成了協議，網景的Blink元素和微軟的Marquee元素被移除[17]。HTML對數學公式的支援最後成為另外一種被稱為MathML的標準。
